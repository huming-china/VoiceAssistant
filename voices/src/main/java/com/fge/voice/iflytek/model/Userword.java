package com.fge.voice.iflytek.model;

public class Userword {
	private String name;

	private String[] words;

	public void setName(String name) {
		this.name = name;
	}

	public String getName() {
		return this.name;
	}

	public void setWords(String[] words) {
		this.words = words;
	}

	public String[] getWords() {
		return this.words;
	}
}
